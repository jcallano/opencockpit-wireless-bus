class WinWingPaced:
    def __init__(self):
        self.device = None
        self.buffer = bytearray(TOTAL_BUFFER_SIZE)
        self.reset_buffer()

    def connect(self):
        try:
            self.device = hid.device()
            self.device.open(VID, PID)
            self.device.set_nonblocking(1)
            print("WinWing MCDU Connected.")
            self.sanitize() 
            return True
        except Exception as e:
            print(f"Connection Error: {e}")
            return False

    def close(self):
        if self.device: self.device.close()

    def sanitize(self):
        """ Clears any stuck state on the device by flooding it with empty packets. """
        print("Sanitizing device state...")
        empty = bytearray(64)
        empty[0] = 0xF2
        for _ in range(20):
            self.device.write(empty)
            time.sleep(0.001)
        time.sleep(0.1)

    def configure(self):
        """ Sends the initialization magic sequence. Only needed once. """
        print("Sending configuration sequence...")
        for cmd in INIT_SEQUENCE:
            self.device.write(bytes(cmd))
            time.sleep(0.01)
        
        # Max Brightness for Keys (0) and Screen (1)
        self.device.write(bytes([0x02, 0x32, 0xbb, 0, 0, 3, 0x49, 0, 255, 0,0,0,0,0]))
        self.device.write(bytes([0x02, 0x32, 0xbb, 0, 0, 3, 0x49, 1, 255, 0,0,0,0,0]))
        time.sleep(0.2) 

    def reset_buffer(self):
        """ Clears local buffer with spaces. """
        for i in range(0, len(self.buffer), 3):
            if i+2 < len(self.buffer):
                self.buffer[i] = 0x42   # Low Byte (W)
                self.buffer[i+1] = 0x00 # High Byte (W)
                self.buffer[i+2] = 0x20 # Space

    def sync_cursor(self):
        """ 
        Manually resets the cursor to 0,0. 
        Only call this ONCE at startup to avoid flickering.
        """
        self.device.write(bytes(CMD_CURSOR_HOME))
        time.sleep(0.2)

    def flush_paced(self):
        """
        Sends the 1024-byte buffer with precise pacing.
        CRITICAL: The 2ms delay between packets prevents input buffer overflow
        on the MCDU chip.
        """
        ptr = 0
        total_len = len(self.buffer)
        
        while ptr < total_len:
            # USB Packets are 64 bytes max
            chunk_size = min(63, total_len - ptr)
            
            packet = bytearray(64)
            packet[0] = 0xF2 # Data Command Header
            packet[1:1+chunk_size] = self.buffer[ptr : ptr+chunk_size]
            
            self.device.write(packet)
            ptr += chunk_size
            
            # THE SECRET SAUCE: 2ms pacing
            time.sleep(PACKET_PACING) 

    def update_char(self, row, col, char, color=0x0084):
        offset = (row * COLS * BYTES_PER_CHAR) + (col * BYTES_PER_CHAR)
        c_low = color & 0xFF
        c_high = (color >> 8) & 0xFF
        self.buffer[offset] = c_low
        self.buffer[offset+1] = c_high
        try:
            self.buffer[offset+2] = ord(char)
        except:
            self.buffer[offset+2] = ord('?')

def main():
    mcdu = WinWingPaced()
    if not mcdu.connect(): return

    try:
        # 1. Init Sequence
        mcdu.configure()
        
        # 2. Sync Cursor (Once)
        mcdu.sync_cursor()
        
        print("Starting Loop...")
        mcdu.reset_buffer()
        mcdu.flush_paced()
        
        counter = 0
        for r in range(ROWS):
            for c in range(COLS):
                mcdu.update_char(r, c, CHAR_TO_DRAW, COLOR_GREEN)
                
                # Send data without resetting cursor
                mcdu.flush_paced()
                
                time.sleep(DELAY_ENTRE_FRAMES)
                sys.stdout.write(f"\r{r},{c} ")
                sys.stdout.flush()
                counter += 1

        print("\nDone.")

    except KeyboardInterrupt:
        print("\nStop.")
    finally:
        mcdu.close()

if __name__ == "__main__":
    main()